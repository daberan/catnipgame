/**
 * Manages keyboard input state for game controls.
 * Tracks the state of movement and action keys.
 */
class Keyboard {
  /** @type {boolean} - Left arrow or 'A' key state */
  LEFT = false;
  /** @type {boolean} - Right arrow or 'D' key state */
  RIGHT = false;
  /** @type {boolean} - Up arrow or 'W' key state */
  UP = false;
  /** @type {boolean} - Down arrow key state */
  DOWN = false;
  /** @type {boolean} - Spacebar key state */
  SPACE = false;
  /** @type {boolean} - 'P' key state */
  P = false;
}
